public class TransferTypeModel extends BaseModel {

    private int typeId;
    private String typeName;
    private String transferListLength;
    private int transferBgResourceId;

    public int getTypeId() {
        return typeId;
    }

    public void setTypeId(int typeId) {
        this.typeId = typeId;
    }

    public int getTransferBgResourceId() {
        return transferBgResourceId;
    }

    public void setTransferBgResourceId(int transferBgResourceId) {
        this.transferBgResourceId = transferBgResourceId;
    }

    public String getTransferListLength() {
        return transferListLength;
    }

    public void setTransferListLength(String transferListLength) {
        this.transferListLength = transferListLength;
    }

    public String getTypeName() {
        return typeName;
    }

    public void setTypeName(String typeName) {
        this.typeName = typeName;
    }

    @Override
    public String toString() {
        return "TransferTypeModel{" +
                "typeId=" + typeId +
                ", typeName='" + typeName + '\'' +
                ", transferListLength='" + transferListLength + '\'' +
                ", transferBgResourceId=" + transferBgResourceId +
                '}';
    }
}
