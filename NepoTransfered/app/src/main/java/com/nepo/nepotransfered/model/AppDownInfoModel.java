public class AppDownInfoModel extends BaseModel {

    private String id;//app的id，和appInfo中的id对应
    private String fileName;//app的软件名称
    private String filePath;
    private String fileType;
    private int fileDownStatus;
    private int fileDownProgress;

    public AppDownInfoModel(String id, String fileName,String filePath,String fileType,int fileDownStatus) {
        this.id = id;
        this.fileName = fileName;
        this.filePath=filePath;
        this.fileType=fileType;
        this.fileDownStatus=fileDownStatus;
    }

    public AppDownInfoModel() {
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getFileName() {
        return fileName;
    }

    public void setFileName(String fileName) {
        this.fileName = fileName;
    }

    public String getFilePath() {
        return filePath;
    }

    public void setFilePath(String filePath) {
        this.filePath = filePath;
    }

    public String getFileType() {
        return fileType;
    }

    public void setFileType(String fileType) {
        this.fileType = fileType;
    }

    public int getFileDownStatus() {
        return fileDownStatus;
    }

    public void setFileDownStatus(int fileDownStatus) {
        this.fileDownStatus = fileDownStatus;
    }

    public int getFileDownProgress() {
        return fileDownProgress;
    }

    public void setFileDownProgress(int fileDownProgress) {
        this.fileDownProgress = fileDownProgress;
    }

    @Override
    public String toString() {
        return "AppDownInfoModel{" +
                "id='" + id + '\'' +
                ", fileName='" + fileName + '\'' +
                ", filePath='" + filePath + '\'' +
                ", fileType='" + fileType + '\'' +
                ", fileDownStatus=" + fileDownStatus +
                ", fileDownProgress=" + fileDownProgress +
                '}';
    }
}
